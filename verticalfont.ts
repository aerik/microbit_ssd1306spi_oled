    function getVcolDataAt(offset: number): number[] {
        let data: number[] = [];
        for (let i = 0; i < 5; i++) {
            data[i] = vchars.charCodeAt(offset + i);//5 bytes, one for each row
        }
        return data;
    }

    export function GetCharacterColValues(inStr: string): number[] {
        let vals: number[] = [];
        for (let i = 0; i < inStr.length; i++) {
            let c = inStr.charCodeAt(i);
            if (c < ASCII_START || c > ASCII_END) {
                c = '?'.charCodeAt(0);
            }
            let offset = (c - ASCII_START) * 5;
            let data: number[] = getVcolDataAt(offset);
            for (let j = 0; j < 5; j++) {
                vals[(i * 5) + j] = data[j];
            }
        }
        return vals;
    }
